    private void mapImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mapImageMouseClicked
        // TODO add your handling code here:
        int x = evt.getX() - Constants.OFFSET_WIDTH_MARK;
        int y = evt.getY() - Constants.OFFSET_HEIGHT_MARK;
        this.marked = true;
        this.positionX = x;
        this.positionY = y;
        AppUtils.setMark(mark, this.mapImage, mapHolder, x, y, this);
    }//GEN-LAST:event_mapImageMouseClicked

    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        // TODO add your handling code here:
        if(this.validates()){
            String name, location;
            Double price;
            int quantity;
            name = this.tfName.getText();
            location = String.valueOf(this.positionX) + "#" 
                    + String.valueOf(this.positionY) + "#" + this.tfLocation.getText();
            price = Double.parseDouble(this.tfPrice.getText());
            quantity = Integer.parseInt(this.tfQuantity.getText());
            
            p.setName(name);
            p.setLocation(location);
            p.setPrice(price);
            p.setQuantity(quantity);
            
            if(p.updateFromDatabase()){
                AppUtils.showDialogSuccess(this, false, "Success to update data with id " 
                        + p.getID(), "Succes to Insert");
            }else{
                 AppUtils.showDialogError(this, false, 
                    "Failed to update data", "SQL Error");
            }
        }else{
            AppUtils.showDialogError(this, false, 
                    "There is an empty field", "VALIDATE Error");
        }
    }//GEN-LAST:event_btnSubmitMouseClicked

