    public void setEmpty(){
        this.tfName.setText("");
        this.tfPrice.setText("");
        this.tfLocation.setText("");
        this.tfQuantity.setText("");
        
        this.marked = false;
        this.mark.setVisible(false);
    }
    
    public void fieldInput(){
        this.tfName.setText(p.getName());
        this.tfPrice.setText(String.valueOf(p.getPrice()));
        this.tfQuantity.setText(String.valueOf(p.getQuantity()));
        String[] location = p.getLocation().split("#");
        
        this.tfLocation.setText(location[2]);
        
        int x = Integer.parseInt(location[0]);
        int y = Integer.parseInt(location[1]);
        
        this.positionX = x;
        
        this.positionY = y;
        
        this.marked = true;
        
        AppUtils.setMark(mark, this.mapImage, mapHolder, x, y, this);
    }
    
    public boolean validates(){
        if(this.tfName.getText().trim().equals("")){
            return false;
        }else if(this.tfLocation.getText().trim().equals("")){
            return false;
        }else if(this.tfPrice.getText().trim().equals("")){
            return false;
        }else if(this.tfQuantity.getText().trim().equals("")){
            return false;
        }else if(!this.marked){
            return false;
        }
        return true;
    }
